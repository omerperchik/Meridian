function makeScore(composite: number, tier: Tier): TrustScore {
  const dim = (name: TrustScore["dimensions"][number]["name"], weight: number, bias = 0) => ({
    name,
    label: name === "performance" ? "Performance" : name.charAt(0).toUpperCase() + name.slice(1),
    weight,
    value: Math.max(0, Math.min(100, composite + bias + (Math.random() * 10 - 5))),
    inputs: {
      security: ["CVE exposure", "Injection resistance", "Permission scope", "Audit results"],
      compliance: ["UAOP coverage", "Disclosure", "Data minimization", "SDK signals"],
      performance: ["Arena benchmarks", "Latency p50/p95", "Task completion", "Accuracy"],
      reliability: ["Uptime", "Error rate", "Graceful degradation", "Incident history"],
      affordability: ["Cost per task", "Pricing transparency", "Efficiency index"],
    }[name],
    updatedAt: "2026-04-15T09:00:00Z",
  });
  const dimensions: TrustScore["dimensions"] = [
    dim("security", 30, 2),
    dim("compliance", 25, -1),
    dim("performance", 20, 1),
    dim("reliability", 15, 0),
    dim("affordability", 10, -3),
  ];
  const tierLabel = ["Auto-discovered", "Claimed", "Premium", "SDK Integrated"][tier];
  // fixed history: 12 points, slight upward trend
  const history = Array.from({ length: 12 }).map((_, i) => ({
    date: new Date(Date.now() - (11 - i) * 30 * 24 * 3600 * 1000).toISOString().slice(0, 10),
    score: Math.max(0, Math.min(100, composite - 4 + i / 2 + (i === 11 ? 0 : Math.sin(i) * 1.5))),
  }));
  return {
    composite,
    tier,
    tierLabel,
    dimensions,
    history,
    lastUpdated: "2026-04-15T09:00:00Z",
  };
}

